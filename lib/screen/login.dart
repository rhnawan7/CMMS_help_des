                            height: 40,
                          ),
                          Padding(
                            padding: const EdgeInsets.fromLTRB(100.0,0.0,50.0,00.0),
                            child: Row(
                              mainAxisAlignment: MainAxisAlignment.spaceBetween,
                              children: [

                                RoundButton(colour: Colors.blue, title: 'Login', onPressed: () {
                              setState(() {
                                isLoading = true;
                              });
                              logIn(email.text, password.text).then((user) {
                                if (user != null) {
                                  print("Login Sucessfull");
                                  setState(() {
                                    isLoading = false;
                                  });
                                  Navigator.push(
                                      context, MaterialPageRoute(builder: (_) => ClientHome()));
